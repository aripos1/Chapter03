package com.javaex.ex03;

public class Rectangle {

	private int width;
	private int height;
	
	
	public final int getWidth() {
		return width;
	}
	public final void setWidth(int width) {
		this.width = width;
	}
	public final int getHeight() {
		return height;
	}
	public final void setHeight(int height) {
		this.height = height;
	}
	
	
}
